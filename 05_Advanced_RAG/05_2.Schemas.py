@dataclass
class EducationInfo:
    company: str
    goal: str
    audience: str
    level: str
    duration: str
    topics: List[str]
    extra: str
    type_counts: Dict[str, int] = field(default_factory=dict)

    def group_count(self, group: str) -> int:
        types = GROUPS.get(group, ())
        return sum(self.type_counts.get(t, 0) for t in types)

    def total_count(self) -> int:
        return sum(self.type_counts.values())

    def active_types(self) -> List[str]:
        active = [t for t in TYPES if self.type_counts.get(t, 0) > 0]
        return active if active else TYPES
